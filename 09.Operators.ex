# Some Math
1 + 1 == 2
10 - 5 == 5
5 * 2 == 10

# `/` Always Returns A Float
10 / 5 == 5.0

# Integer Division
div(10, 2) == 5

# Remainder Of Division
rem(10, 3) == 1

# Boolean Operators, Expecting A Boolean As First Argument
true and true == true
false or true == true

1 || true == 1
false && 1 == false
nil && 20 == nil
!true == false

# Comparisons
1 == 1
1 != 1
1 < 2

# Strict Comparison
1 == 1.0  # True
1 === 1.0 # False

